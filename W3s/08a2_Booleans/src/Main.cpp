/*
C++ Boolean Data Types

Tipe data boolean dideklarasikan dengan kata 
kunci bool dan hanya dapat mengambil nilai 
true atau false. Ketika nilainya 
dikembalikan, true = 1 dan false = 0.
*/ 

int main(int argc, char const *argv[])
{
bool isCodingFun = true;
bool isFishTasty = false;
cout << isCodingFun << endl;  // Outputs 1 (true)
cout << endl << isFishTasty;  // Outputs 0 (false)

return 0;
}

